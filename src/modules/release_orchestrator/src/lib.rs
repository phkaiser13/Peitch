// This struct mirrors the ErrorPayload message from rpc_data.proto.
// In a real build system, this would be generated by prost-build.
#[derive(Clone, PartialEq, ::prost::Message)]
pub struct ErrorPayload {
    #[prost(string, tag = "1")]
    pub code: ::prost::alloc::string::String,
    #[prost(string, tag = "2")]
    pub message: ::prost::alloc::string::String,
    #[prost(string, tag = "3")]
    pub details: ::prost::alloc::string::String,
}

const RELEASE_NAMESPACE: &str = "ph-releases";

async fn handle_start(config: StartConfig) -> Result<()> {
    println!("ğŸš€ Starting new release for app: {}", config.app);

    let client = Client::try_default()
        .await
        .context("Failed to create Kubernetes client")?;
    let releases: Api<PhgitRelease> = Api::namespaced(client, RELEASE_NAMESPACE);

    // --- Build Canary Strategy from CLI flags ---
    let mut steps = Vec::new();
    if let Some(steps_str) = config.steps {
        for s in steps_str.split(',') {
            let weight = s.trim().parse::<i32>().context(format!("Invalid step value: '{}'. Steps must be comma-separated integers.", s))?;
            steps.push(CanaryStep {
                set_weight: weight,
                analysis_window: config.analysis_window.clone(),
            });
        }
    } else {
        // Default to a single step of 100% if --steps is not provided
        steps.push(CanaryStep {
            set_weight: 100,
            analysis_window: config.analysis_window.clone(),
        });
    }

    let mut metrics = Vec::new();
    if let Some(metric_str) = config.metric {
        // Assuming the metric is passed as a JSON string for simplicity
        let metric: Metric = serde_json::from_str(&metric_str)
            .context("Failed to parse --metric flag. It must be a valid JSON string representing a single metric.")?;
        metrics.push(metric);
    }

    let analysis = if !metrics.is_empty() {
        Some(Analysis {
            interval: config.analysis_window.clone().unwrap_or_else(|| "5m".to_string()),
            threshold: 5, // Default value
            max_failures: 2, // Default value
            metrics,
        })
    } else {
        None
    };

    let canary_strategy = CanaryStrategy {
        steps,
        analysis,
        auto_promote: true, // Defaulting to true for now
    };

    let strategy = Strategy {
        type_: "Canary".to_string(),
        canary: Some(canary_strategy),
        blue_green: None,
    };

    // --- Construct the PhgitRelease Resource ---
    let release = PhgitRelease {
        metadata: ObjectMeta {
            name: Some(config.app.clone()),
            ..Default::default()
        },
        spec: PhgitReleaseSpec {
            app_name: config.app.clone(),
            version: config.image, // Using image as version for now
            strategy,
        },
        status: Some(PhgitReleaseStatus {
            phase: Some("Progressing".to_string()),
            ..Default::default()
        }),
    };

    // --- Create the Resource in Kubernetes ---
    releases
        .create(&PostParams::default(), &release)
        .await
        .context(format!("Failed to create PhgitRelease resource for app '{}'", config.app))?;

    println!("âœ… PhgitRelease resource '{}' created successfully with a canary strategy.", config.app);
    Ok(())
}

